# what is a list?

# how to create a list?
fruits = ["apple", "banana", "cherry"]

# how to access items in a list?  
print(fruits[0])

# how to modify items in a list?
fruits[0] = "orange"

# how to add items to a list?
fruits.append("orange")

# how to remove items from a list?
fruits.remove("banana")

# how to sort a list?
fruits.sort()

# how to reverse a list?
fruits.reverse()

# how to find the length of a list?
print(len(fruits))

# how to find the index of an item in a list?
print(fruits.index("apple"))

# how to find the number of times an item appears in a list?
print(fruits.count("apple"))

# how to find the maximum value in a list?
print(max(fruits))

# how to find the minimum value in a list?
print(min(fruits))

#how to loop through a list?
for fruit in fruits:
    print(fruit)

