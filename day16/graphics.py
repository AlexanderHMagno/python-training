#turtle graphics what is it?
#turtle graphics is a library that allows you to create graphics in python
#it is a simple way to create graphics and is often used for educational purposes
#it is a part of the standard library and does not need to be installed

import turtle

turtle.shape("turtle")
turtle.color("red")
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.right(90)
turtle.forward(100)
turtle.done()